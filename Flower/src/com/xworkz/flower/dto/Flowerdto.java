package com.xworkz.flower.dto;

public class Flowerdto {
	private String nameOfTheFlower;
	private String type;
	private int price;
	private int size;
	
	public void setNameOfTheFlower(String nameOfTheFlower) {
		this.nameOfTheFlower = nameOfTheFlower;
	}
	public String getNameOfTheFlower() {
		return nameOfTheFlower;
	}
	public void setType(String type) {
		this.type = type;
	}
	public String getType() {
		return type;
	}
	
	public void setPrice(int price) {
		this.price = price;
	}
	public int getPrice() {
		return price;
	}
	
	public void setSize(int size) {
		this.size = size;
	}
	public int getSize() {
		return size;
	}
	

}
